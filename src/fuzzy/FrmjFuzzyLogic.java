    void fuzzifica() {
        fis.setVariable("Distancia", sensorDistancia);
        fis.setVariable("Velocidade", sensorVelocidade);
        fis.evaluate();
        Variable resp = fis.getVariable("pressaodofreio");
        Double saida = resp.getValue();
        jLabelPressaoFreio.setText(String.valueOf(saida));
        dialog.repaint();
    }
    
